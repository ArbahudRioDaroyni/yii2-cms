							<h2 class="title">Layanan Unggulan Rawat Jalan RS St. Carolus</h2>
							<h4 class="sub-title">oleh : RS St. Carolus</h4>
							<span class="date">Post on 26 September, 2018</span>
						</div>
						<div class="detail-text">
<p>Rawat jalan adalah pelayanan medis tanpa harus dirawat inap. <br />Berikut ini daftar layanan unggulan rawat jalan RS St. Carolus.</p>
<p>Selamat datang di <strong>Unit Rawat Jalan (URJ)</strong> Rumah Sakit St Carolus.<br />Percayakan kesehatan anda kepada kami, dokter spesialis kami akan melayani anda dengan ketelitian dan penjelasan informasi yang diperlukan dalam mencapai kesehatan pasien yang prima dan ulung.</p>
<p>Ditunjang oleh tenaga keperawatan, tenaga para medis dan administrasi yang siap melayani anda, kami berharap kunjungan anda merupakan kunjungan yang memberikan kesejukan dalam proses penyembuhan anda.</p>
<p>&ldquo;RS. St. Carolus terletak di lokasi strategis di pusat kota DKI Jakarta. Sangat mudah di akses dari berbagai penjuru dengan menggunakan kendaraan pribadi maupun umum.&rdquo; Carolus memperoleh predikat RS Sayang Ibu dan Bayi Terbaik dari Kemenkes dan Kementerian Pemberdayaan Perempuan dan Perlindungan Anak dua kali berturut-turut, yakni tahun 2007 dan 2010, di lingkup Rumah Sakit Jakarta Pusat. Kami selalu berusaha memperbaiki prosedur operasional dan sistem layanan yang berorientasi pada pasien.</p>
<p>Rumah sakit kami terletak di Jalan Salemba Raya Nomor 41, dengan luas sekitar 50.000 meter persegi. Dalam jangka menengah, kami berencana, mengembangkan unit-unit yang bisa dikembangkan secara fisik, apakah dengan pembaharuan gedung atau pembangunan pusat layanan baru untuk memaksimalkan jasa terhadap setiap pasien .</p>
<p>Daftar layanan unggulan yang disediakan antara lain, mulai dari klinik &ndash; klinik spesialis seperti bedah, pusat rehabilitasi, pusat kesehatan, pengobatan penyakit, kecantikan, gizi dan Gigi akan dibahas lebih lanjut pada sub topik selanjutnya.</p>
<p><strong>&nbsp;</strong></p>
<h2>Layanan Unggulan Rawat Jalan RS St. Carolus</h2>
<img src="/upload/static/598ae9c46f68d1502276036.jpg" alt="Pelayanan Rawat Jalan Rs. St. Carolus">
<p><strong>&nbsp;</strong></p>
<p><strong>Unit Rawat Jalan</strong> adalah pelayanan <a href="https://id.wikipedia.org/wiki/Medis">medis</a> kepada seorang <a href="https://id.wikipedia.org/wiki/Pasien">pasien</a> untuk tujuan <a href="https://id.wikipedia.org/wiki/Pengamatan">pengamatan</a>, <a href="https://id.wikipedia.org/wiki/Diagnosis">diagnosis</a>, <a href="https://id.wikipedia.org/wiki/Pengobatan">pengobatan</a>, <a href="https://id.wikipedia.org/wiki/Rehabilitasi">rehabilitasi</a>, dan <a href="https://id.wikipedia.org/w/index.php?title=Pelayanan_kesehatan&amp;action=edit&amp;redlink=1">pelayanan kesehatan</a> lainnya, tanpa mengharuskan pasien tersebut di <a href="https://id.wikipedia.org/wiki/Rawat_inap">rawat inap</a> atau upaya yang dilakukan bagi penderita yang mempunyai keluhan kesehatan untuk memeriksakan atau mengatasi gangguan keluhan kesehatannya dengan mendatangi tempat pelayanan kesehatan modern atau tradisional tanpa menginap, termasuk mendatangkan petugas medis ke rumah pasien, membeli obat atau melakukan pengobatan sendiri. (Diambil dari pengertian kamus besar).&nbsp; Keuntungannya, pasien tidak perlu mengeluarkan biaya untuk menginap (<a href="https://id.wikipedia.org/wiki/Opname">opname</a>).</p>
<p>Salah satu fungsi rumah sakit menurut Friedman dan Roemar yaitu:</p>
<ul>
<li>Mendiagnosa dan memberikan pengobatan.</li>
<li>Memberikan pelayanan pasien URJ</li>
<li>Memberikan pendidikan kepada tenaga yang berkerja di Rumah sakit.</li>
<li>Tempat penelitian dibidang Kedokteran.</li>
<li>Mengadakan pelayanan pencegahan dan meningkatkan derajat kesehatan masyarakat sekitarnya.</li>
</ul>
<p>URJ merupakan salah satu bagian terpenting dari fungsi rumah sakit, dalam peraturan Menteri Kesehatan RI No. 159 B /Menkes/Per/ ll/1988 Pasal 9 tentang fungsi Rumah Sakit , anatara lain :</p>
<ul>
<li>Menyediakan dan menyelenggarakan : Pelayanan Medik, Pelayanan Penunjang Medik, Pelayanan Perawatan, Pelayanan Rehabilitasi.</li>
<li>Sebagai tempat pendidikan dan atau latihan tenaga medik dan paramedik.</li>
<li>Sebagai tempat penelitian dan pengembangan ilmu dan teknologi dibidang kesehatan.</li>
</ul>
<p>Menurut Faste, Pelayanan URJ adalah salah satu bentuk dari pelayanan kedokteran yang secara sederhana. Pelayanan kedokteran yang sederhana. Pelayanan kedokteran yang disediakan untuk pasien tidak dalam rawat inap (<em>Hospitalization</em>).</p>
<p>Keputusan Menteri Kesehatan No.66 / Menkes / ll /1987 yang di maksud adalah pelayanan terhadap orang yang masuk rumah sakit , untuk keperluan observasi, diagnosa, pengobatan, rehabilitasi medik dan pelayanan kesehatan lainnya tanpa tinggal diruang rawat inap.</p>
<h2>Daftar Layanan Rawat Jalan RS St. Carolus</h2>
<img src="/upload/static/599acb3e7eddd1503316798.jpg" alt="Jenis Perawatan Rawat Jalan Rs. St. Carolus" width="600">
<p>&nbsp;</p>
<p>Jenis Layanan yang dimiliki URJ Rumah Sakit St. Carolus, antara lain di dalam A. Klinik Spesialis / Sub Spesialis, yaitu Bedah Anak, Bedah Digestif, Bedah Mulut, Bedah Plastik, Bedah Saluran Kemih, Bedah Syaraf, Bedah Thorax, Bedah Tumor, Bedah Tulang, Bedah Umum, Bedah Vasculer, Rehabilitasi Medik, Kesehatan Anak, Kebidanan &amp; Kandungan, Penyakit Dalam, Penyakit Jantung, Penyakit Paru, Penyakit Hati &amp; Pencernaan, Penyakit Ginjal, Penyakit Mata, Penyakit Kulit &amp; Kelamin, Kecantikan, Penyakit Syaraf, Penyakit Jiwa, Gizi, Gigi yang terdiri dari; Prostodonti, Periodontologi, Pedodonti, Orthodonti. B. Lain &ndash; lain yaitu; Psikolog, Gigi Umum.</p>
<p>Fasilitas yang telah disediakan antara lain, ruang tunggu pasien yang nyaman, ruang menyusui, ruang pemeriksaan dokter spesialis, ruang tindakan, ruang observasi termasuk pasien day care, ruang penunjang medik yang tersedia seperti: treadmill, elektrokardiografi (EKG), Elektroencephalografi (EEG), audiologi, spirometri, uroflometri, biometri. Klinik yang tersedia antara lain, klinik kecantikan, klinik kebidanan, klinik laktasi, klinik anak.</p>
<h2>Prosedur &amp; Alur URJ RS. St. Carolus</h2>
<ol>
<li>Pasien datang mendaftarkan diri di loket pendaftaran (sebutkan dokter yang akan dikunjungi&nbsp; / bila anda belum tahu akan ke dokter siapa akan dibantu oleh petugas.)</li>
<li>Bagi peserta asuransi atau perusahaan pelanggan yang bekerjasama dengan RS. St. Carolus , mohon pada saat pendaftaran menunjukkan kartu asuransi atau surat jaminan kepada petugas.</li>
<li>Ruang Periksa Dokter</li>
<li>Apotik, bila pasien pulang</li>
<li>Kalau perlu, periksa Penunjang Medik ( Laboratorium, Radiologi, dll.)</li>
<li>Rawat Inap.</li>
</ol>
<p>URJ RS. St. Carolus salah satu unit pelayanan yang dimiliki dan diadakan di RS. St Carolus yang diberikan kepada pasien untuk tujuan pengamatan diagnosis, pengobatan,rehabilitasi, dan pelayanan kesehatan lainnya, tanpa mengharuskan pasien tersebut untuk di rawat inap.</p>
<p>Pusat Rawat Jalan merupakan fasilitas yang efisien, di mana pasien mendapatkan perawatan medis terbaik dan pulang ke rumah pada hari yang sama. Mulai dari pendaftaran, persiapan bedah, hingga proses pemulihan dan saat pulang dari rumah sakit,&nbsp;semuanya dilaksanakan di tempat yang sama, sehingga pasien tidak perlu<s> </s>dirawat inap di rumah sakit, yang akan menghemat biaya. Layanan&nbsp;kami dilengkapi peralatan medis untuk mendukung teknik bedah termutakhir dan bedah katarak. Pasien memiliki akses ke ahli bedah, ahli anestesi yang berpengalaman, perawat terakreditasi&nbsp;dan staf lainnya, yang memberikan perawatan personal dan sangat profesional untuk mempercepat masa penyembuhan pasca-operasi. Dengan tim operasi kami yang terlatih dalam berbagai sub-spesialisasi tertentu, seluruh program bedah kami hadirkan secara terpadu, guna memfasilitasi transisi yang lancar dari diagnosis sampai perawatan.</p>
<p>Perawatan dan keselamatan pasien merupakan fokus utama di URJ Rumah Sakit St. Carolus. Kami juga secara teratur melakukan pengujian ketat terhadap jumlah bakteri, partikel dan laju pertukaran udara untuk mewujudkan lingkungan yang aman dan kondusif bagi para pasien kami.</p>
<p>Pendaftaran URJ dilakukan melalui nomor 021 &ndash; 2356 7990, atau 7981 , 7982 dibuka setiap hari pada jam kerja, manfaatkan juga kemudahan terbaru berupa pendaftaran Rawat jalan melalui aplikasi WhatsApp, Format pendaftaran: #Nama dokter yang dituju #tanggal berobat (pagi / sore) #Nama pasien #Carabayar (JKN / umum / jaminan) #NomorRM (kalau ada) #Tanggal lahir #No HP yang bisa dihubungi (bisa lebih dari 1). Pendaftaran via whatsapp dilayani pkl 07.00 s/d 20.00 WIB, <strong>alternative lainnya kunjungi website kami, www.rscarolus.or.id</strong></p>
